<p>③ 회사의 서비스 제공을 위해 계약한 CP와의 계약 종료 및 변경, 신규서비스 개시 등의 사유로 서비스의 내용이 변경되거나, 서비스가 종료될 수도 있습니다. 서비스 변경 사항 또는 종료는 서비스 초기화면이나 개별 서비스의 화면 또는 공지사항 란에 게시하여 이용자에게 알려드리겠습니다. 이용자께 중대한 영향을 미치는 서비스 변경 사항이나 종료는 회원 아이디로 사용하는 이메일 주소로 이메일을 발송하거나, 전자쪽지를 보내는 방법 등으로 개별적으로 알려드리겠습니다.</p>


<h4>제13조 (서비스의 이용제한)</h4><p>① 회사는 이용자가 서비스를 이용 중에 본 약관 제15조의 내용을 위반하거나, 다음 각 호에 해당하는 행위를 한 경우, 이용자의 서비스 이용을 제한할 수 있습니다. </p>
<ul>
<li>1. 미풍양속을 저해하는 비속한 게시물을 게시하는 행위</li>
<li>2. 타인의 저작권, 명예 또는 권리 등을 침해하는 게시물을 게시하는 행위</li>
<li>3. 정보통신 윤리위원회 등 관련 공공기관의 시정 요구가 있는 경우</li>
<li>4. 타 이용자의 서비스 이용을 방해하는 행위</li>
   <li>5. 정상적인 서비스 운영에 방해가 되는 행위</li>
   <li>6. 기타 이용자의 서비스 이용에 불법적인 요소가 포함된다고 회사가 판단하는 경우② 전①항에 의하여 회사가 서비스 이용을 제한하는 경우, 회사는 해당 회원에게 서비스 이용제한의 사유 및 소명 가능 기간 등을 고지합니다. </li>
</ul>
<p>③ 서비스 이용이 제한된 이용자는 회사의 고객센터를 이용하여 이용제한에 대한 이의를 제기할수 있으며, 이용자의 이의제기가 있을 경우 회사는 이에 대한 재심의를 거쳐 이용자에 대한 이용제한 해제여부를 결정할 수 있습니다. 이 경우 회사는 해당 결과를 이용자가 요청하는 방법(이메일 또는 유선통화)으로 즉시 이용자에게 통지합니다. </p>
<p>④ 서비스의 이용제한이 이용자의 고의 또는 과실에 의한 경우가 아닌 경우, 회사는 이용제한 기간 만큼에 해당하는 기간을 유료서비스의 이용기간(유효기간)에 추가하여 제공합니다.</p>
 
<h4>제14조 (이용자에 대한 통지)</h4>
<p>회사는 서비스 웹페이지(www.timeforpoetry.com) 또는 이용자가 회사에 제공한 이메일을 통하여 이용자에 대한 통지를 합니다.</p>
 
<h4>제15조 (이용자의 의무)</h4>
<p>① 이용자는 이 약관에서 규정하는 사항과 기타 회사가 공지하는 사항 및 관계법령을 준수하여야 하며, 회사의 업무에 방해가 되는 행위나 회사의 명예를 손상시키는 행위 등을 해서는 안됩니다. </p>
<p>② 이용자는 회사의 사전승낙 없이 서비스를 이용하여 영업활동을 할 수 없으며, 그 영업활동으로 인하여 발생하는 문제에 대하여 회사는 책임을 지지 않습니다. 또한 이용자의 이와 같은 영업활동으로 인하여 회사에 손해가 발생한 경우, 이용자는 회사에 대한 손해배상의 책임을 집니다. </p>
<p>③ 이용자는 서비스를 이용함에 있어 다음 각 호에 해당하는 행위를 하여서는 안됩니다.</p>
<ul>
<li>1. 서비스 이용과정에서 얻은 정보를 회사의 사전승낙 없이 복제, 배포, 출판 및 방송 등에 사용하거나 제3자에게 제공하는 행위 </li>
<li>2. 타인의 특허, 상표, 영업비밀, 저작권 등의 지적재산권을 침해하는 내용을 게시하거나 타인에게 유포하는 행위 </li>
<li>3. 서비스의 이용권한, 기타 이용 계약상 지위를 타인에게 양도·증여하는 행위나, 담보로 제공하는 행위3. 공공질서 및 미풍양속에 위반되는 저속, 음란한 내용의 정보, 문장, 도형 등을 전송, 게시, 전자메일 또는 기타의 방법으로 타인에게 유포하는 행위 </li>
<li>4. 모욕적이거나 위협적이어서 타인의 프라이버시를 침해할 수 있는 내용을 전송, 게시, 전자메일 또는 기타의 방법으로 타인에게 유포하는 행위 </li>
<li>5. 범죄와 결부된다고 객관적으로 판단되는 행위 </li>
<li>6. 다른 이용자의 개인정보를 수집 또는 저장하는 행위</li>
<li>7. 회사의 임직원 또는 관계자를 사칭하는 행위 </li>
<li>8. 회사의 동의 없이 서비스 또는 이에 포함된 소프트웨어의 일부를 복사, 수정, 배포, 판매, 양도, 대여, 담보제공하거나 타인에게 그 이용을 허락하는 행위와 소프트웨어를 역설계하거나 소스 코드의 추출을 시도하는 등 서비스를 복제, 분해 또는 모방하거나 기타 변형하는 행위</li>
<li>9. 잘못된 방법으로 서비스의 제공을 방해하거나 회사가 안내하는 방법 이외의 다른 방법을 사용하여 서비스에 접근하는 행위</li>
<li>10. 다른 이용자를 희롱 또는 위협하거나, 특정 이용자에게 지속적으로 고통 또는 불편을 주는 행위 </li>
  <li>11. 기타 법령에 위배되는 행위</li>
</ul>
<p>④혹시라도 이용자가 관련 법령, 회사의 모든 약관 또는 정책을 준수하지 않는다면, 회사는 여러분의 위반행위 등을 조사하거나 이용자의 서비스 이용을 잠시 또는 계속하여 중단하거나, 재가입에 제한을 둘 수도 있습니다.</p>

<p>⑤회사는 법령에서 정하는 기간 동안 이용자가 서비스를 이용하기 위해 로그인 혹은 접속한 기록이 없는 경우 이용자가 등록한 전자우편, SMS 등 기타 유효한 수단으로 통지 후 이용자의 정보를 파기하거나 분리 보관할 수 있으며, 이로 인해 서비스 이용을 위한 필수적인 정보가 부족할 경우 이용계약이 해지될 수도 있습니다. </p>

<h4>제16조 (회사의 의무)</h4>
<p>① 회사는 이용자가 쾌적한 환경에서 서비스를 이용할 수 있도록 신의성실의 원칙에 따라 노력하여야 할 의무가 있습니다.</p>
<p>② 회사는 관계 법령과 약관에 위반되는 행위를 하지 않으며, 안정적인 서비스를 제공하여야 할 의무가 있습니다.</p>
<p>③ 회사는 이용자가 안전하게 서비스를 이용할 수 있도록 개인정보처리방침을 시행하고 있으며, 이에 따라 이용자의 개인정보를 보호하여야 할 의무가 있습니다. 회사의 개인정보처리방침은 서비스의 웹페이지( www.timeforpoetry.com )를 통하여 확인할 수 있습니다.</p>
 
 
<h4>제17조 (유료서비스)</h4>
<p>① 이용권 구매</p>
<ul>
  <li>1. 이용권을 구매하고자 하는 회원은 서비스에 로그인한 후 마이페이지의 이용권 관리를 통해 구매하실 수 있습니다. 월 구매가격은 4800원입니다. 3개월 묶음 구매는 13,000원입니다. 단, 이는 회사의 정책상 변경될 수 있습니다. </li>
  <li>2. 회사의 정책에 의하여 규정된 단위로만 구매가 가능합니다.</li>
  <li>3. 이용자는 서비스의 관리자 페이지에서 구매내역 및 결제 내역 등을 확인하실 수 있습니다. </li>
  <li>4. 이용자는 방송 페이지상에서 구매한 이용권의 사용범위를 확인하실 수 있으며 결제이력은 어떠한 경우에도 취소할 수 없습니다. </li>
  <li>5. 이용자가 구매한 이용권은 제3자에게 임의로 양도하거나 처분할 수 없습니다. </li> 
</ul>
<p>② 이용자가 회사가 제공하는 유료서비스를 이용하는 경우 이용대금을 납부한 후 이용하는 것을 원칙으로 합니다. 회사가 제공하는 유료서비스에 대한 이용요금의 결제 방법은 핸드폰결제, 신용카드결제, 일반전화결제, 계좌이체, 무통장입금, 선불전자지급수단 결제 등이 있으며 각 유료서비스마다 결제 방법의 차이가 있을 수 있습니다. 매월 정기적인 결제가 이루어지는 서비스의 경우 이용자 개인이 해당 서비스의 이용을 중단하고 정기 결제의 취소를 요청하지 않는 한 매월 결제가 이루어집니다.</p>
<p>③ 회사는 결제의 이행을 위하여 반드시 필요한 이용자의 개인정보를 추가적으로 요구할 수 있으며, 이용자는 회사가 요구하는 개인정보를 정확하게 제공하여야 합니다.</p>

<h4>제18조 (이용권 구매 취소, 환불)</h4>
<p>① 이용자 개인의 귀책사유로 이용요금을 환불하는 경우 일반적인 방법은 아래와 같습니다. 단, 여러분이 본 약관을 위반하여 회사가 여러분의 서비스 이용을 제한하거나 계약을 해지하는 경우 회사는 여러분에게 일체의 환불을 하지 않습니다.</p><p>② 이용자는 이용권을 구매한 후 하루 이내에 서비스의 마이페이지상에서 구매취소 및 환불이 가능하며, 일부라도 사용한 경우에는 회사가 정한 별도의 이용대금의 결제와 동일한 방법으로 환불절차(동일한 방법으로 환불이 불가할 경우에는 별도의 방법으로 환불)를 거쳐 구매한 이용권을 신청일로부터5영업일 이내에 환불 받을 수 있습니다.</p>

