            <h2>이용약관</h2>
<h3>제 1장</h3> 
<h4>제1조 (목적)</h4><p>이 약관은 “시와 시 사이 시간” (이하 “회사”라 합니다)이 제공하는 시간 서비스(이하 “서비스”라 합니다)를 이용함에 있어 회사와 이용자 간의 권리∙의무 및 기타 제반 사항을 규정함을 목적으로 합니다.</p>
<h4>제2조 (정의)</h4><p>이 약관에서 사용하는 용어의 정의는 다음과 같습니다.</p>
<p>① “시간서비스”라 함은 오디오 콘텐츠 및 페이지의 개설, 등록, 스트리밍, 구독 및 관련 자료 공유가 가능하도록 회사가 제공하는 전송 서비스(웹/모바일 포함)를 의미합니다. </p>
<p>② “회원”이라 함은 서비스에 접속하여 회사가 요청하는 개인정보를 제공하고 서비스 회원 가입 절차를 완료한 자를 의미합니다. </p>
<p>③ “비회원”이라 함은 회원에 가입하지 않고 서비스를 이용하는 자를 의미합니다. </p>
<p>④ “이용자”라 함은 서비스에 접속하여 이 약관에 따라 회사가 제공하는 서비스를 받는 회원 또는 비회원을 말합니다. </p>
<p>⑤ “이용권” 이라 함은 방송 운영자의 유료 콘텐츠를 이용할 수 있는 이용료 단위입니다.</p>
<p>⑥ “보이스 크리에이터(낭독자)”라 함은 서비스를 통하여 회사가 정한 절차를 통하여 방송 또는 전송 등록 신청을 완료하고, 회사의 인증을 받은 방송 이용자를 말합니다.</p>
<p>⑦ “게시물”라 함은 “회원”이 “서비스”를 이용함에 있어 “서비스”상에 방송에 포함된 음성, 음향, 동영상, 문서 외 게시판에 생성한 글, 사진, 동영상 및 각종 파일과 링크 등을 의미합니다.</p>
<h4>제3조 (약관의 효력 및 변경)</h4><p>① 이 약관은 서비스 웹페이지( www.timeforpoetry.com )를 통하여 이를 공지함으로써 약관 시행일로부터 그 효력이 발생합니다. </p>
  <p>② 회사가 약관을 변경할 경우에는 적용일자 및 변경사유를 명시하여 현행약관과 함께 서비스 웹페이지 초기화면에 그 적용일자 10일(이용자에게 불리한 변경 또는 중대한 사항의 변경은 30일) 이전부터 적용일자 이후 상당한 기간 동안 공지하고, 기존 “회원”에게는 변경될 약관, 적용일자 및 변경사유(중요내용에 대한 변경인 경우 이에 대한 설명을 포함)를 전자우편주소로 발송합니다. </p>
<p>③ 회사가 전 항에 따라 공지 또는 통지를 하면서 공지 또는 통지일로부터 개정약관 시행일 7일 후까지 거부의사를 표시하지 아니하면 승인한 것으로 본다는 뜻을 명확하게 고지하였음에도 여러분의 의사표시가 없는 경우에는 변경된 약관을 승인한 것으로 봅니다. 여러분이 개정약관에 동의하지 않을 경우 여러분은 제12조 제1항에 따라 이용계약을 해지할 수 있습니다.</p>
 
<h4>제4조 (약관 외 준칙)</h4><p>이 약관에 명시되지 아니한 사항에 대하여는 관계법령 및 일반 관례에 따릅니다.
본 약관에 규정되지 않은 사항에 대해서는 관련법령 또는 회사가 정한 서비스의 개별 이용약관, 운영정책 및 규칙 등(이하 ‘세부지침’)의 규정에 따릅니다. 또한 본 약관과 세부지침의 내용이 충돌할 경우 세부지침에 따릅니다.</p>

<h3>제2장 시간 계정</h3>
<h4>제5조 (회원가입)</h4><p>① 이용자는 회사가 정한 가입 양식에 따라 회원정보를 기입한 후 이 약관에 동의한다는 의사표시와 필요한 경우 회사가 이메일 주소 또는 전화번호를 인증하면 회사가 승낙 함으로써 회원가입이 이루어집니다.</p>
  <p>② 회원가입 신청은 온라인 가입양식에 회사가 요청하는 필수 정보들을 기재하여 신청합니다. </p>
  <p>③ 회사는 이용자가 회원가입 신청시 기재하는 모든 가입정보를 실제 데이터인 것으로 간주합니다. 이에 고의•과실 여부를 불문하고 허위의 정보를 기재한 이용자는 법적인 보호를 받을 수 없으며, 서비스제공의 제한을 받을 수 있습니다. </p>
  <p>④ 회원은 본 조 제②항에 의한 등록사항에 변경이 있는 경우, 즉시 서비스를 통하여 그 변경사항을 회사에 제공하여야 합니다.</p>

<h4>제6조 (회원가입 신청에 대한 승낙 및 거부)</h4>
  <p>① 회사는 제5조의 회원가입 신청에 대하여 특별한 사정이 없는 한 접수순서에 따라 이를 승낙합니다. </p>
  <p>② 회사는 다음 각 호에 해당하는 경우 회원가입 신청에 대한 승낙을 제한할 수 있고, 그 사유가 해소될 때까지 승낙을 유보할 수 있습니다. 특히, 회원가입을 하려는 자가 14세 미만인 경우에는 부모님 등 법정대리인의 동의가 있는 경우에만 회원가입을 할 수 있습니다.</p>
  <ul>
  <li>1. 서비스 관련 설비의 용량이 부족한 경우 </li>
  <li>2. 기술상 장애사유가 있는 경우 </li>
  <li>3. 가입신청자가 본 약관 제8조 2항에 의거하여 이전에 회원 자격을 상실한 적이 있는 경우</li>
<li>4. 다른 사람의 명의나 이메일 주소 등 개인정보를 이용하여 회원가입을 하려 한 경우 </li>
<li>5. 등록내용에 허위, 기재누락, 오기가 있는 경우</li>
<li>6. 기타 관련법령에 위배되거나 회사가 정한 기준에 반하는 경우</li>
 </ul>
<p>③ 만약, 이용자가 위 조건에 위반하여 회원가입을 한 것으로 판명된 때에는 회사는 즉시 당사자의 서비스 이용을 중단하거나 아이디를 삭제하는 등 적절한 제한을 할 수 있습니다.</p>

<p>④ 본 조의 규정에 따른 회원가입신청 시, 회사는 전문기관을 통한 회원의 실명확인 및 본인인증을 요청할 수 있습니다.</p>
 
<h4>제7조 (회원정보의 변경)</h4>
<p>① 회원은 언제든지 서비스 웹페이지를 이용하여 자신의 가입 정보들을 변경할 수 있습니다. 단, 서비스 관리를 위해 필요한 실명, 아이디 등은 수정이 불가능합니다.</p>
<p>② 회원은 회원가입신청 시 기재한 사항이 변경되었을 경우, 온라인으로 수정을 하거나 전자우편 기타 방법으로 회사에 변경 사실을 알려야 합니다. 변경사항을 반영하지 않거나 회사에 알리지 않아 발생한 불이익에 대해 회사는 책임지지 않습니다.</p>
 <p> ③ 회원의 아이디와 비밀번호에 관한 관리책임은 회원에게 있으며, 이를 제3자가 이용하도록 하여서는 안됩니다.</p>
  <p>④ 회원은 아이디 및 비밀번호가 도용되거나 제3자가 사용하고 있음을 인지하는 경우에는 이를 즉시 회사에 통지하고 회사의 안내에 따라야 합니다. 이 경우 해당 회원이 회사에 그 사실을 통지하지 않거나 통지한 경우에도 회사의 안내에 따르지 않아 발생한 불이익에 대해서 회사가 책임지지 않습니다.</p>
 
<h4>제8조 (회원 탈퇴 및 자격상실) </h4>
  <p>① 회원은 회사에 언제든지 탈퇴를 요청할 수 있으며, 회원의 탈퇴요청이 있을 시에 회사는 해당 회원을 탈퇴 처리합니다. </p>
  <p>② 회사는 다음 각호에 해당하는 경우 회원의 자격을 제한 및 삭제할 수 있습니다. </p>
  <ul>
  <li>1. 회원가입 신청시 허위의 정보를 기재한 경우 </li>
  <li>2. 설비에 여유가 없거나 기술상 지장이 있는 경우 </li>
  <li>3. 회원이 본 약관 제13조 및 제15조를 위반한 경우</li>
  <li>4. 기타 회사가 필요하다고 인정하는 경우</li>
  </ul>
  <p>③ 회사가 전②항에 따라 회원자격을 상실시킨 경우 당해 회원에게 이의 사실과 사유를 통지합니다. 다만, 당해 회원이 이의 통지를 받은 날로부터 30일 이내에 소명하고, 그 내용이 정당한 경우 회사는 즉시 회원의 자격을 복구합니다. </p>
  <p>④ 회사는 회원이 탈퇴하거나 회원자격을 상실하는 경우 회원의 개인정보를 정보통신망이용촉진및 정보보호등에관한 법률 등 관계법령에서 정한 범위와 절차에 따라 파기합니다. </p>
<p>⑤ 이용계약이 해지되면 법령 및 개인정보 처리방침에 따라 이용자의 정보를 보유하는 경우를 제외하고는 이용자의 정보나 이용자가 작성한 게시물 등 모든 데이터는 삭제됩니다. 다만, 이용자가 작성한 게시물이 제3자에 의하여 스크랩 또는 다른 공유 기능으로 게시되거나, 이용자가 제3자의 게시물에 댓글 등 게시물을 추가하는 등의 경우에는 다른 이용자의 정상적 서비스 이용을 위하여 필요한 범위 내에서 서비스 내에 삭제되지 않고 남아 있게 됩니다.</p>

<p>⑥ 유료서비스 이용계약의 해지는 이용자의 서비스 해지 신청 및 회사의 승낙에 의해 성립하게 되고, 환불할 금액이 있는 경우 환불도 이루어 지게 됩니다. 다만 각 개별 유료서비스에서 본 약관과 다른 계약해지 방법 및 효과를 규정하고 있는 경우 각 개별약관의 규정에 따릅니다.</p>

<p>⑦ 이용계약이 해지된 경우라도 이용자는 다시 회사에 대하여 이용계약의 체결을 신청할 수 있습니다. 다만, 일부 서비스의 경우 다시 이용계약을 체결함에 있어 시간적 제한 등이 따를 수 있습니다.</p>
 
<h4>제9조 (회원에 대한 통지)</h4>
<p>① 회사는 서비스 웹페이지 초기화면에 대한 공지, 회원이 가입신청 당시 제공한 이메일 주소, 전화번호 등을 이용한 방법으로 회원에 대한 통지를 할 수 있습니다. </p>
<p>② 회사는 불특정 다수에 대한 통지의 경우 서비스 웹페이지 초기 화면에 1주일간 해당 내용을 기재함으로써, 개별통지에 갈음할 수 있습니다.</p>

<h3>제3장 서비스의 이용</h3>
<h4>제10조 (서비스의 종류 및 변경)</h4>
<p>① 회사가 제공하는 서비스의 종류는 아래와 같습니다. 1. 회사는 시 낭독 음원 스트리밍 서비스, 온라인 콘텐츠 업로드 서비스, 위치기반서비스 등의 서비스를 제공합니다. 이용자는 스마트폰의 어플리케이션 스토어 등에서 서비스를 다운받아 설치하거나 직접 PC에 설치 혹은 웹페이지에 접속하여 서비스를 이용할 수 있습니다. 그런데 회사는 이용자가 원하는 다양한 서비스를 시시각각 제공하기 때문에 서비스의 자세한 내용은 별도로 알려드릴 수밖에 없습니다. 이러한 회사의 사정을 이해하여 주시길 바라며, 회사도 개별적인 서비스 이용방법을 어플리케이션 스토어와 각 서비스의 Q&A센터, 해당 안내 및 고지사항에서 더 상세하게 안내하고 있으니 언제든 확인하여 주시기 바랍니다. </p>
<p>② 회사가 제공하는 서비스의 종류는 회사의 경영정책상 변경될 수 있으며, 변경된 내용은 서비스를 통하여 공지합니다.</p>
<p>③ 회사는 위 ②항에 따른 서비스의 변경으로 인해 회원이 서비스이용을 통해 기대하는 수익을 상실한 것에 대하여 어떠한 책임도 부담하지 않습니다.</p>
<p>④ 회사는 더 나은 서비스의 제공을 위하여 여러분에게 서비스의 이용과 관련된 각종 고지, 관리 메시지 및 기타 광고를 비롯한 다양한 정보를 서비스에 표시하거나 이용자의 메일 계정으로 직접 발송할 수 있습니다.</p>
<p>⑤ 여러분이 서비스를 이용하는 과정에서 Wi-Fi 무선인터넷을 사용하지 않고, 가입하신 이동통신사의 무선인터넷에 연결하여 이용하는 경우 이동통신사로부터 여러분에게 별도의 데이터 통신요금이 부과되는 점을 유의하여 주시기 바랍니다. 서비스 이용 과정에서 발생하는 데이터 통신요금은 여러분이 여러분의 비용과 책임 하에 이동통신사에 납부하셔야 합니다. 데이터 통신요금에 대한 자세한 안내는 여러분이 가입하신 이동통신사에 문의하시기 바랍니다.
